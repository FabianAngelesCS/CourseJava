package documentacion.arithmeticOperators;

public class whatIsArithmeticOperators {

    /*
 ---¿Que son los operadores aritmeticos¿---


Los operadores aritméticos en Java se utilizan para realizar operaciones matemáticas básicas entre com.variables y
valores numéricos. Aquí tienes una lista de los operadores aritméticos en Java:

Suma:
+: Suma dos operandos.

Resta:
-: Resta el operando derecho del operando izquierdo.

Multiplicación:
*: Multiplica dos operandos.

División:
/: Divide el operando izquierdo por el operando derecho. Si ambos operandos son enteros, la división se realiza
 como una división entera (se truncará el resultado a un número entero).

Módulo:
%: Devuelve el residuo de la división del operando izquierdo por el operando derecho.

Estos operadores se pueden utilizar con com.variables numéricas (enteros o de punto flotante) o valores numéricos
literales para realizar cálculos matemáticos. Aquí tienes un ejemplo de cómo se utilizan estos operadores:

Ejemplos:
------------------------------------------------------------------------------------------------------------------------
int x = 10;
int y = 3;

int suma = x + y; // 13
int resta = x - y; // 7
int multiplicacion = x * y; // 30
int division = x / y; // 3 (debido a la división entera)
int modulo = x % y; // 1

double a = 10.0;
double b = 3.0;

double resultado = a / b; // 3.3333333333333335 (resultado de la división de punto flotante)

------------------------------------------------------------------------------------------------------------------------

Es importante recordar que la división entre enteros truncará el resultado a un número entero si ambos operandos son
enteros. Si se desea una división de punto flotante, al menos uno de los operandos debe ser de punto flotante.

     */
}
