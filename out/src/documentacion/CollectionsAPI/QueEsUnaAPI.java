/*
Una API (Application Programming Interface) es un conjunto de definiciones y protocolos que permiten la comunicación
entre diferentes aplicaciones o servicios. Actúa como un intermediario que permite que un software acceda a las
funcionalidades o datos de otro software sin necesidad de conocer su implementación interna.

En términos simples:
API es como un contrato que define cómo dos sistemas deben interactuar entre sí.
Proporciona un conjunto de métodos y herramientas que los desarrolladores pueden usar para realizar solicitudes y
recibir respuestas de un sistema externo.
Por ejemplo, cuando una aplicación usa Google Maps para mostrar un mapa, está utilizando la API de Google Maps para
solicitar los datos del mapa y luego integrar esa información en su propia aplicación.

Tipos de API:
* APIs REST (basadas en HTTP): Utilizan peticiones HTTP para acceder y manipular datos.
* APIs SOAP (Simple Object Access Protocol): Un protocolo más formal y basado en XML.
* APIs de biblioteca: Se encuentran dentro de bibliotecas o frameworks, permitiendo a los desarrolladores usar sus
  funciones o clases.

Las APIs son esenciales en el desarrollo moderno, ya que permiten que diferentes sistemas, incluso si están construidos
con tecnologías diferentes, puedan trabajar juntos.
 */
