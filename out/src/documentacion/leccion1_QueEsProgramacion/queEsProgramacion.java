/*
¿QUE ES PROGRAMACIÓN?

La programación es el proceso de crear instrucciones detalladas que le indican a una computadora  que hacer para
resolver un problema o realizar una tarea específica.
Estas instrucciones se escriben en un lenguaje de programación que la computadora
puede interpretar o compilar para ejecutar dichas acciones.
Aunque no solo se trata de dar indicaciones, pues también se deben:

- **Abstraer problemas** del mundo real en pasos lógicos para que una maquina pueda reseguirlos.
- **Diseñar soluciones** eficientes y claras, que incluyan manejos de datos, control de flujo(decisiones, bucles) y la
reutilización de código.
- **Probar** las soluciones y corregir errores(debugging) para asegurar que fusionen como se espera.

Una definición más completa podría ser algo como: "La programación es el acto de crear instrucciones precisas y lógicas
que resuelven problemas"
 */
