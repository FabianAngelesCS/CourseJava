public class whatIsRelationalOperators {

    /*
    ----¿Qué son los operadores relacionales?---

Los operadores relacionales, también conocidos como operadores de comparación, se utilizan en Java para comparar
dos valores y determinar la relación entre ellos. Estos operadores devuelven un valor booleano (true o false) según
el resultado de la comparación. Aquí tienes una lista de los operadores relacionales en Java:

Igualdad:
==: Comprueba si dos valores son iguales.

Desigualdad:
!=: Comprueba si dos valores son diferentes.

Mayor que:
>: Comprueba si el valor de la izquierda es mayor que el valor de la derecha.

Mayor o igual que:
>=: Comprueba si el valor de la izquierda es mayor o igual que el valor de la derecha.

Menor que:
<: Comprueba si el valor de la izquierda es menor que el valor de la derecha.

Menor o igual que:
<=: Comprueba si el valor de la izquierda es menor o igual que el valor de la derecha.

Estos operadores se utilizan comúnmente en expresiones condicionales, bucles y otras estructuras de control para tomar
decisiones basadas en condiciones. Por ejemplo:

Ejemplos
-----------------------------------------------------------------------------------------------------------------------
int x = 5;
int y = 10;

boolean esIgual = (x == y); // false
boolean esDiferente = (x != y); // true
boolean esMayor = (x > y); // false
boolean esMayorOIgual = (x >= y); // false
boolean esMenor = (x < y); // true
boolean esMenorOIgual = (x <= y); // true

-----------------------------------------------------------------------------------------------------------------------
Estas expresiones evalúan la relación entre las com.variables x e y utilizando los operadores relacionales y almacenan los
 resultados en com.variables booleanas.

     */
}
