package com.RelationalOperators;

public class EjemploOperadores {

    public static void main(String[] args) {
        int numeroDesarrolladores = 2;
        int numeroProyectos = 10;

        double salarioEmpledo = 260;

        boolean esBienPagado = false;
        boolean estoyContento = false;

        System.out.println(numeroDesarrolladores == numeroProyectos);
        System.out.println(numeroProyectos == salarioEmpledo);
        System.out.println(esBienPagado == estoyContento);

    }

}
