package com.loopWhile;

public class EjemploWhile {

    static void whileEjemplo(){

        int numeroImpreso = 0;
        while (numeroImpreso <= 10){
            System.out.println(numeroImpreso);
            numeroImpreso++;
        }
    }

    public static void main(String[] args) {
        whileEjemplo();
    }
}
