package com.Encapsulacion;

public class ModificadoresAcceso {

    /*
----¿Cuaoles son los mofioficadores de acceso?----

En Java, hay cuatro modificadores de acceso que se utilizan para controlar el acceso a clases, com.variables, métodos y
constructores en diferentes niveles de visibilidad dentro de un programa. Estos modificadores de acceso son:

public:
Los elementos marcados como public son accesibles desde cualquier otra clase en el mismo paquete o desde cualquier otra
clase en diferentes paquetes. Es el nivel de acceso más alto en Java.

protected:
Los elementos marcados como protected son accesibles desde la misma clase, desde las clases del mismo paquete y también
desde las clases hijas (subclases), incluso si están en diferentes paquetes.

default (o package-private):
Si no se especifica ningún modificador de acceso, se considera el nivel de acceso predeterminado, también conocido como
"package-private". Los elementos con este modificador son accesibles solo desde las clases del mismo paquete.

private:
Los elementos marcados como private son accesibles solo dentro de la misma clase. No son visibles ni accesibles desde
ninguna otra clase, incluidas las clases del mismo paquete.

Estos modificadores de acceso permiten controlar la visibilidad y el alcance de los miembros de una clase, lo que ayuda
a garantizar la encapsulación y la seguridad de los datos en una aplicación Java.
     */
}
